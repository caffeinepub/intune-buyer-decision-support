export function getSizeAllocations(
  styleCode: string,
  totalQty: number,
): SizeAllocation[] {
  let template = sizeTemplates.standard;
  if (styleCode.includes("TOP") || styleCode.includes("DRS")) {
    template = sizeTemplates.topHeavy;
  } else if (styleCode.includes("OUT") || styleCode.includes("BOT")) {
    template = sizeTemplates.bottomHeavy;
  }
  return template.map((s) => ({
    ...s,
    suggestedRebuyQty: Math.round((s.sizeContributionPct / 100) * totalQty),
  }));
}

